    class excelFilter extends javax.swing.filechooser.FileFilter {//filters out possible files
        private String[] extentions ={".xlsx",".xls",".xml",".csv" ,".ods" ,".ost"};//list file types
    
    @Override
    public boolean accept(File file){//checks for each extension in the array
    if (file.exists()){
        for (String ext : extentions){
            if(file.getName().toLowerCase().endsWith(ext))
            {return true;}
        }
    }
        return false;
    }
    @Override
    
    public String getDescription() {//shows the user the acceptable types of files

        // This description will be displayed in the dialog,
        // hard-coded = ugly, should be done via I18N
        return "Spreadsheets (*.xml/xlsx)";
        }
    }
