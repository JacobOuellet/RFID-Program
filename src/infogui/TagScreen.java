    private void UnlockButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UnlockButtonActionPerformed
        try{
            // takes out the data that between each . in the set reader password txt box and then puts it into an array and passes it to main
            String readerkey; 
            readerkey = changeTagPass.getText();
            int[] keys = new int[6];
            if(readerkey.length() <= 6 & !readerkey.equals("")){
                while(readerkey.length() < 6){
                    readerkey += '0';
                }
             for(int x = 0; x < 6; x++){
      
                    keys[x] = (int)readerkey.charAt(x);
            }
             Main.SetTagKeys(keys);   
            }
            else {
                JOptionPane.showMessageDialog(null, "Invalid password: Password must be between 1 and 6 chracters inclusive, ", "InfoBox: " + "Password character(s) invalid!", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(null, "Invalid password: Password must be between 1 and 6 chracters inclusive, ", "InfoBox: " + "Password character(s) invalid!", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_UnlockButtonActionPerformed

